def send_pdf_to_feishu(pdf_path):
    """发送 PDF 到飞书"""
    import subprocess
    import base64
    
    # 读取 PDF 文件
    with open(pdf_path, 'rb') as f:
        pdf_data = base64.b64encode(f.read()).decode('utf-8')
    
    # 发送带附件的消息
    cmd = [
        "openclaw", "message", "send",
        "--channel", "feishu",
        "--target", FEISHU_USER_ID,
        "--message", f"📊 锋哥持仓报告已生成～\n\n生成时间：{datetime.now().strftime('%Y-%m-%d %H:%M')}\n\n请查收附件 PDF 文件 (◍•ᴗ•◍)",
        "--buffer", f"data:application/pdf;base64,{pdf_data}",
        "--filename", os.path.basename(pdf_path)
    ]
    
    try:
        subprocess.run(cmd, check=True)
        print("✅ PDF 已发送到飞书")
    except Exception as e:
        print(f"❌ 发送失败：{e}")

