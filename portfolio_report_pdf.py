def get_realtime_price(quote_ctx, symbol, market):
    """获取实时价格"""
    try:
        if market == 'US':
            code = f"US.{symbol}"
        else:
            code = f"HK.{symbol}"
        
        ret, data = quote_ctx.get_market_snapshot(code)
        if ret == RET_OK and len(data) > 0:
            return float(data['last_price'][0])
        return None
    except Exception as e:
        print(f"获取 {symbol} 价格失败：{e}")
        return None

def create_pie_chart(data, title, filename):
    """创建饼图"""
    plt.figure(figsize=(10, 6))
    
    labels = [item['label'] for item in data]
    sizes = [abs(item['value']) for item in data]
    colors_list = ['#2ecc71' if item['value'] >= 0 else '#e74c3c' for item in data]
    
    plt.pie(sizes, labels=labels, colors=colors_list, autopct='%1.1f%%', startangle=90)
    plt.title(title, fontsize=14, fontweight='bold')
    plt.axis('equal')
    
    plt.savefig(filename, dpi=150, bbox_inches='tight')
    plt.close()
    return filename

def create_bar_chart(data, title, filename, xlabel='', ylabel='盈亏 (%)'):
    """创建柱状图"""
    plt.figure(figsize=(12, 6))
    
    labels = [item['label'] for item in data]
    values = [item['value'] for item in data]
    colors_list = ['#2ecc71' if v >= 0 else '#e74c3c' for v in values]
    
    x_pos = range(len(labels))
    plt.bar(x_pos, values, color=colors_list)
    plt.xticks(x_pos, labels, rotation=45, ha='right')
    plt.xlabel(xlabel)
    plt.ylabel(ylabel)
    plt.title(title, fontsize=14, fontweight='bold')
    plt.axhline(y=0, color='black', linestyle='-', linewidth=0.5)
    plt.grid(axis='y', alpha=0.3)
    
    plt.savefig(filename, dpi=150, bbox_inches='tight')
    plt.close()
    return filename

