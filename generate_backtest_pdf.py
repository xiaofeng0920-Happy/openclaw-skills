def create_metrics_table(results):
    """创建指标表格"""
    data = [
        ['指标', '周度调仓', '月度调仓', '差异'],
        ['总收益率', f"{results['weekly']['total_return']*100:.1f}%", f"{results['monthly']['total_return']*100:.1f}%", 
         f"{(results['weekly']['total_return']-results['monthly']['total_return'])*100:+.1f}%"],
        ['年化收益率', f"{results['weekly']['annual_return']*100:.1f}%", f"{results['monthly']['annual_return']*100:.1f}%", 
         f"{(results['weekly']['annual_return']-results['monthly']['annual_return'])*100:+.1f}%"],
        ['夏普比率', f"{results['weekly']['sharpe']:.2f}", f"{results['monthly']['sharpe']:.2f}", 
         f"{results['weekly']['sharpe']-results['monthly']['sharpe']:+.2f}"],
        ['最大回撤', f"{results['weekly']['maxdd']*100:.1f}%", f"{results['monthly']['maxdd']*100:.1f}%", 
         f"{(results['weekly']['maxdd']-results['monthly']['maxdd'])*100:+.1f}%"],
        ['交易次数', f"{results['weekly']['trades']:,}", f"{results['monthly']['trades']:,}", "-"],
        ['交易成本', f"${results['weekly']['costs']:,}", f"${results['monthly']['costs']:,}", 
         f"+${results['weekly']['costs']-results['monthly']['costs']:,}"]
    ]
    
    table = Table(data, colWidths=[1.5*inch, 1.5*inch, 1.5*inch, 1.5*inch])
    font_name = 'Chinese' if chinese_font_path else 'Helvetica'
    table.setStyle(TableStyle([
        ('BACKGROUND', (0, 0), (-1, 0), colors.HexColor('#2c3e50')),
        ('TEXTCOLOR', (0, 0), (-1, 0), colors.whitesmoke),
        ('ALIGN', (0, 0), (-1, -1), 'CENTER'),
        ('FONTNAME', (0, 0), (-1, 0), font_name),
        ('FONTSIZE', (0, 0), (-1, 0), 11),
        ('BOTTOMPADDING', (0, 0), (-1, 0), 12),
        ('BACKGROUND', (0, 1), (-1, -1), colors.HexColor('#f8f9fa')),
        ('FONTNAME', (0, 1), (-1, -1), font_name),
        ('GRID', (0, 0), (-1, -1), 1, colors.HexColor('#dee2e6')),
        ('ROWBACKGROUNDS', (0, 1), (-1, -1), [colors.white, colors.HexColor('#f8f9fa')]),
    ]))
    return table

