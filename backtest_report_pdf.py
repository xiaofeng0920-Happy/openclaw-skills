def load_markdown(path):
    """加载 Markdown 文件"""
    if not os.path.exists(path):
        return None
    with open(path, 'r', encoding='utf-8') as f:
        return f.read()

def create_cover_page(c, title, subtitle, date):
    """创建封面"""
    # 背景渐变
    c.setFillColor(colors.HexColor('#1a1a2e'))
    c.rect(0, 0, A4[0], A4[1], fill=True, stroke=False)
    
    # 标题
    c.setFillColor(colors.HexColor('#ffffff'))
    c.setFont('Helvetica-Bold', 32)
    c.drawCentredString(A4[0]/2, A4[1]/2 + 50, title)
    
    # 副标题
    c.setFont('Helvetica', 16)
    c.setFillColor(colors.HexColor('#a0a0a0'))
    c.drawCentredString(A4[0]/2, A4[1]/2 - 20, subtitle)
    
    # 日期
    c.setFont('Helvetica', 14)
    c.drawCentredString(A4[0]/2, A4[1]/2 - 80, date)
    
    # 装饰线
    c.setStrokeColor(colors.HexColor('#4a90e2'))
    c.setLineWidth(2)
    c.line(A4[0]/2 - 100, A4[1]/2 - 100, A4[0]/2 + 100, A4[1]/2 - 100)
    
    # 底部信息
    c.setFillColor(colors.HexColor('#666666'))
