def send_email(pdf_path):
    """发送邮件"""
    print(f"📧 正在发送邮件到 {to_email}...")
    
    # 创建邮件
    msg = MIMEMultipart()
    msg['From'] = from_email
    msg['To'] = to_email
    msg['Subject'] = f"📊 锋哥回测报告 - {datetime.now().strftime('%Y-%m-%d')}"
    
    # 邮件正文
    body = f"""
锋哥好～

这是小巴为您生成的回测报告 PDF，请查收！

📅 报告日期：{datetime.now().strftime('%Y-%m-%d %H:%M')}
📄 报告内容：
  - 核心业绩指标 (总收益 +120.59%, 年化 27.96%)
  - 策略说明 (趋势跟踪 + 动量选股)
  - 业绩分析 (收益表现、风险控制、交易特征)
  - 策略优势与改进建议
  - 下一步行动计划

📈 回测期间：2023-2026 (3 年)
💰 初始资金：$1,000,000 USD
🎯 最终价值：$2,205,854

祝投资顺利！(◍•ᴗ•◍)

---
小巴 Portfolio Agent
"""
    
    msg.attach(MIMEText(body, 'plain', 'utf-8'))
    
    # 添加 PDF 附件
    with open(pdf_path, "rb") as attachment:
        part = MIMEBase('application', 'octet-stream')
        part.set_payload(attachment.read())
    
    encoders.encode_base64(part)
    part.add_header(
        'Content-Disposition',
        f'attachment; filename="锋哥回测报告_{datetime.now().strftime("%Y%m%d")}.pdf"'
    )
    msg.attach(part)
    
    # 发送邮件
    try:
        server = smtplib.SMTP_SSL(smtp_server, smtp_port)
        server.login(from_email, password)
        server.send_message(msg)
        server.quit()
        print("✅ 邮件发送成功！")
        return True
    except Exception as e:
        print(f"❌ 发送失败：{e}")
        return False

if __name__ == "__main__":
    pdf_path = generate_pdf()
    if pdf_path and os.path.exists(pdf_path):
        if send_email(pdf_path):
            print("✅ 回测报告 PDF 生成并发送完成！")
        else:
            sys.exit(1)
    else:
        print("❌ 生成失败")
        sys.exit(1)
