def parse_metrics(md_content):
    """解析 Markdown 中的指标"""
    metrics = {}
    lines = md_content.split('\n')
    for line in lines:
        if '**总收益率**' in line:
            metrics['total_return'] = line.split('|')[1].strip()
        elif '**年化收益率**' in line:
            metrics['annual_return'] = line.split('|')[1].strip()
        elif '**夏普比率**' in line:
            metrics['sharpe'] = line.split('|')[1].strip()
        elif '**最大回撤**' in line:
            metrics['max_drawdown'] = line.split('|')[1].strip()
        elif '**胜率**' in line:
            metrics['win_rate'] = line.split('|')[1].strip()
        elif '**最终价值**' in line:
            metrics['final_value'] = line.split('|')[1].strip()
    return metrics

